int	ft_str_is_numeric(char *str)
{
	int	c;

	c = 0;
	while (str[c] != 0)
		c++;
	c--;
	if (c == 0)
		return (1);
	while (c >= 0)
	{
		if (str[c] >= 48 && str[c] <= 57)
			continue ;
		else
			return (0);
	}
	return (1);
}
