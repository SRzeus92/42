char	*ft_strlcpy(char *dest, char *src, unsigned int n)
{
	int	i;

	i = 0;
	while (i < n - 1)
	{
		dest[i] = src[i];
		i++;
	}
	dest[n - 1] = 0;
	while (src[i] != 0)
		i++;
	i++;
	return (i);
}
