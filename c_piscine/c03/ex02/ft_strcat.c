char	*ft_strcat(char *dest, char *src)
{
	int		i;
	int		ls;

	i = 0;
	ls = 0;
	while (dest[i] != 0)
		i++;
	while (src[ls] != 0)
	{
		dest[i] = src[ls];
		ls++;
		i++;
	}
	dest[i] = 0;
	return (dest);
}
