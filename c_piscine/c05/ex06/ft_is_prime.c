int	ft_is_prime(int nb)
{
	int	n;

	n = 2;
	if (nb < 2)
		return (0);
	if (nb == 2)
		return (1);
	while (n <= (nb / n) + 1)
	{
		if ((nb % n) == 0)
			return (0);
		n++;
	}
	return (1);
}
