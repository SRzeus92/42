void	ft_rev_int_tab(int *tab, int size)
{
	int	buffer;
	int	i;

	i = 0;
	while (i < size / 2)
	{
		buffer = tab[size - 1 - i];
		tab[size - 1 - i] = tab[i];
		tab[i] = buffer;
		i++;
	}	
}
