#include <unistd.h>

int	main(int argc, char **argv)
{
	int	i;
	int	j;

	j = 1;
	if (argc < 2)
		return (0);
	while (j <= argc)
	{
		i = 0;
		while (str[j][i] != 0)
		{
			write(1, &str[j][i], 1);
			i++;
		}
		j++;
	}
}
