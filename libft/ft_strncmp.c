#include "libft.h"

int	ft_strncmp(char *str1, char *str2, unsigned int n)
{
	int	i;

	i = 0;
	if (n == 0)
		return (0);
	while (str1[i] && str1[i] == str2[i] && i < n - 1)
		i++;
	return (str1[i] - str2[i]);
}
